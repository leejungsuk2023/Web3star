-- ---------------------------------------------------------------------------
-- 10) RPC: 포인트 수동 지급/차감 (단일 트랜잭션 + mining_logs + 감사)
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.admin_adjust_points(p_user_id uuid, p_delta bigint, p_reason text)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  cur bigint;
  new_pt bigint;
  rsn text := left(coalesce(nullif(trim(p_reason), ''), 'admin_adjust'), 500);
BEGIN
  PERFORM public._admin_assert();
  IF p_user_id IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'message', 'Invalid user');
  END IF;
  IF p_delta = 0 THEN
    RETURN jsonb_build_object('ok', false, 'message', 'Delta cannot be zero');
  END IF;

  SELECT coalesce(point, 0) INTO cur FROM public.users WHERE id = p_user_id FOR UPDATE;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'message', 'User not found');
  END IF;

  new_pt := cur + p_delta;
  IF new_pt < 0 THEN
    RETURN jsonb_build_object('ok', false, 'message', 'Insufficient points', 'current', cur, 'delta', p_delta);
  END IF;

  UPDATE public.users SET point = new_pt WHERE id = p_user_id;
  INSERT INTO public.mining_logs (user_id, amount, type, slot_number)
  VALUES (p_user_id, p_delta::int, 'ADMIN_ADJUST', NULL);

  PERFORM public._admin_write_audit(
    'adjust_points',
    p_user_id,
    jsonb_build_object('delta', p_delta, 'reason', rsn, 'balance_after', new_pt)
  );

  RETURN jsonb_build_object('ok', true, 'balance', new_pt);
EXCEPTION
  WHEN OTHERS THEN
    RETURN jsonb_build_object('ok', false, 'message', SQLERRM);
END;
$$;

GRANT EXECUTE ON FUNCTION public.admin_adjust_points(uuid, bigint, text) TO authenticated;

