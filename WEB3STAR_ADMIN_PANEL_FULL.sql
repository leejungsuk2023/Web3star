CREATE OR REPLACE FUNCTION public.admin_decide_withdrawal(p_id bigint, p_approve boolean, p_note text DEFAULT NULL)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  w record;
  cur bigint;
  new_pt bigint;
BEGIN
  PERFORM public._admin_assert();

  SELECT * INTO w FROM public.withdrawal_requests WHERE id = p_id FOR UPDATE;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'message', 'Request not found');
  END IF;
  IF w.status <> 'pending' THEN
    RETURN jsonb_build_object('ok', false, 'message', 'Already decided');
  END IF;

  IF p_approve THEN
    SELECT coalesce(point, 0) INTO cur FROM public.users WHERE id = w.user_id FOR UPDATE;
    IF NOT FOUND THEN
      RETURN jsonb_build_object('ok', false, 'message', 'User missing');
    END IF;
    IF cur < w.amount THEN
      RETURN jsonb_build_object('ok', false, 'message', 'User has insufficient points');
    END IF;
    new_pt := cur - w.amount;
    UPDATE public.users SET point = new_pt WHERE id = w.user_id;
    INSERT INTO public.mining_logs (user_id, amount, type, slot_number)
    VALUES (w.user_id, (-w.amount)::int, 'WITHDRAW_APPROVED', NULL);
  END IF;

  UPDATE public.withdrawal_requests
  SET status = CASE WHEN p_approve THEN 'approved' ELSE 'rejected' END,
      decided_at = now(),
      decided_by = auth.uid(),
      note = coalesce(nullif(trim(p_note), ''), note)
  WHERE id = p_id;

  PERFORM public._admin_write_audit(
    'withdrawal_decision',
    w.user_id,
    jsonb_build_object('request_id', p_id, 'approved', p_approve, 'amount', w.amount)
  );

  RETURN jsonb_build_object('ok', true);
EXCEPTION
  WHEN OTHERS THEN
    RETURN jsonb_build_object('ok', false, 'message', SQLERRM);
END;
$$;

GRANT EXECUTE ON FUNCTION public.admin_decide_withdrawal(bigint, boolean, text) TO authenticated;

-- ---------------------------------------------------------------------------
-- 13) RPC: 통계 요약
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.admin_stats_summary()
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  issued bigint;
  spent bigint;
  user_count_active bigint;
  user_count_suspended bigint;
  total_mining bigint;
  today_mining bigint;
  abnormal_users bigint;
BEGIN
  PERFORM public._admin_assert();

  SELECT coalesce(sum(amount), 0) INTO issued FROM public.mining_logs WHERE amount > 0;
  SELECT coalesce(sum(-amount), 0) INTO spent FROM public.mining_logs WHERE amount < 0;
  SELECT count(*) INTO user_count_active FROM public.users WHERE account_status = 'active';
  SELECT count(*) INTO user_count_suspended FROM public.users WHERE account_status = 'suspended';
  SELECT coalesce(sum(amount), 0) INTO total_mining FROM public.mining_logs WHERE type = 'MINING' AND amount > 0;
  SELECT coalesce(sum(amount), 0) INTO today_mining
  FROM public.mining_logs
  WHERE type = 'MINING' AND amount > 0 AND created_at >= date_trunc('day', now());

  SELECT count(*)::bigint INTO abnormal_users
  FROM (
    SELECT 1
    FROM public.mining_logs m
    WHERE m.type = 'MINING' AND m.created_at > now() - interval '24 hours'
    GROUP BY m.user_id
    HAVING count(*) > 12
  ) s;

  RETURN jsonb_build_object(
    'ok', true,
    'points_positive_sum', issued,
    'points_negative_abs_sum', spent,
    'active_users', user_count_active,
    'suspended_users', user_count_suspended,
    'total_mining_sum', total_mining,
    'today_mining_sum', today_mining,
    'abnormal_mining_users_24h', abnormal_users
  );
END;
$$;

GRANT EXECUTE ON FUNCTION public.admin_stats_summary() TO authenticated;

