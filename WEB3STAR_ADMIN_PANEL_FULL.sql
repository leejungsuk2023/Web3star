-- ---------------------------------------------------------------------------
-- 5) 민감 컬럼 직접 수정 방지 (일반 사용자가 role 등 변경 불가)
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.users_protect_sensitive_columns()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  is_admin boolean;
BEGIN
  IF TG_OP <> 'UPDATE' THEN
    RETURN NEW;
  END IF;
  IF NEW.role IS DISTINCT FROM OLD.role
     OR NEW.account_status IS DISTINCT FROM OLD.account_status
     OR NEW.mining_disabled IS DISTINCT FROM OLD.mining_disabled THEN
    -- SQL Editor 등: JWT 없음(auth.uid() NULL)이면 대시보드 직접 실행으로 간주해 허용(최초 관리자 지정)
    IF auth.uid() IS NULL THEN
      RETURN NEW;
    END IF;
    SELECT EXISTS (SELECT 1 FROM public.users u WHERE u.id = auth.uid() AND u.role = 'admin')
    INTO is_admin;
    IF NOT is_admin THEN
      RAISE EXCEPTION 'Forbidden: role/status/mining flags can only be changed by admins'
        USING ERRCODE = '42501';
    END IF;
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tr_users_protect_sensitive ON public.users;
CREATE TRIGGER tr_users_protect_sensitive
  BEFORE UPDATE ON public.users
  FOR EACH ROW
  EXECUTE FUNCTION public.users_protect_sensitive_columns();

-- ---------------------------------------------------------------------------
-- 6) 내부: 관리자 여부 (클라이언트에 GRANT 하지 않음)
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public._admin_assert()
RETURNS void
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  IF auth.uid() IS NULL THEN
    RAISE EXCEPTION 'Not authenticated' USING ERRCODE = '28000';
  END IF;
  IF NOT EXISTS (SELECT 1 FROM public.users u WHERE u.id = auth.uid() AND u.role = 'admin') THEN
    RAISE EXCEPTION 'Admin only' USING ERRCODE = '42501';
  END IF;
END;
$$;

REVOKE ALL ON FUNCTION public._admin_assert() FROM PUBLIC;

