CREATE OR REPLACE FUNCTION public.admin_set_user_role(p_user_id uuid, p_role text)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  PERFORM public._admin_assert();
  IF p_role NOT IN ('user', 'vip', 'admin') THEN
    RETURN jsonb_build_object('ok', false, 'message', 'Invalid role');
  END IF;
  IF p_user_id = auth.uid() AND p_role <> 'admin' THEN
    RETURN jsonb_build_object('ok', false, 'message', 'Cannot demote yourself');
  END IF;
  UPDATE public.users SET role = p_role WHERE id = p_user_id;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'message', 'User not found');
  END IF;
  PERFORM public._admin_write_audit('set_user_role', p_user_id, jsonb_build_object('role', p_role));
  RETURN jsonb_build_object('ok', true);
END;
$$;

