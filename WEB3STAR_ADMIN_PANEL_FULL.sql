-- Supabase Dashboard → SQL Editor 에서 한 번 실행 (또는 migrations 로 분할 배포).
--
-- 적용 후 첫 관리자 지정 (본인 이메일로 교체):
--   UPDATE public.users SET role = 'admin' WHERE email = 'your@email.com';
--
-- 클라이언트는 anon 키만 사용합니다. 크로스 사용자 조회/수정은 아래 RPC만 호출하세요.

-- ---------------------------------------------------------------------------
-- 1) users 컬럼 확장
-- ---------------------------------------------------------------------------
ALTER TABLE public.users
  ADD COLUMN IF NOT EXISTS role text NOT NULL DEFAULT 'user'
    CHECK (role IN ('user', 'vip', 'admin'));

ALTER TABLE public.users
  ADD COLUMN IF NOT EXISTS account_status text NOT NULL DEFAULT 'active'
    CHECK (account_status IN ('active', 'suspended', 'deleted'));

ALTER TABLE public.users
  ADD COLUMN IF NOT EXISTS mining_disabled boolean NOT NULL DEFAULT false;

COMMENT ON COLUMN public.users.role IS 'user | vip | admin — admin만 RPC로 타인 변경 가능';
COMMENT ON COLUMN public.users.account_status IS 'active | suspended | deleted';
COMMENT ON COLUMN public.users.mining_disabled IS 'true면 채굴/적립 로직에서 차단(앱에서 확인 필요)';

-- ---------------------------------------------------------------------------
-- 2) 감사 로그 (직접 INSERT 금지 — RPC에서만 기록)
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.admin_audit_log (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  admin_id uuid NOT NULL REFERENCES public.users (id) ON DELETE CASCADE,
  action text NOT NULL,
  target_user_id uuid REFERENCES public.users (id) ON DELETE SET NULL,
  payload jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS admin_audit_log_created_at_idx ON public.admin_audit_log (created_at DESC);
CREATE INDEX IF NOT EXISTS admin_audit_log_target_idx ON public.admin_audit_log (target_user_id);

ALTER TABLE public.admin_audit_log ENABLE ROW LEVEL SECURITY;

-- 관리자도 클라이언트에서 직접 읽지 않음 — 목록은 RPC로만 (선택: service_role 대시보드 조회)
DROP POLICY IF EXISTS "admin_audit_no_client" ON public.admin_audit_log;
CREATE POLICY "admin_audit_no_client" ON public.admin_audit_log FOR ALL USING (false);

-- ---------------------------------------------------------------------------
-- 3) 환전/출금 요청 (승인 플로우)
